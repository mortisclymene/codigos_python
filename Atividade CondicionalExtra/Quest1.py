num = int(input("Digite um número de 0 até 100 :"))

if 0 <= num <= 25:
    print("Você escolheu um número entre 0 e 25")

elif 26 <= num <= 50:
    print("Você escolheu um número entre 26 e 50")

elif 51 <= num <= 75:
    print("Você escolheu um número entre 51 e 75")

elif 76 <= num <= 100:
    print("Você escolheu um número entre 76 e 100")